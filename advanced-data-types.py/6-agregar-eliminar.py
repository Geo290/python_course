mascotas = ["el", "yo", "super yo"]

mascotas.insert(1,"ella") #inserta en un indice especifico
mascotas.append("nosotros") #inserta al final de la lista
mascotas.remove("el") #remueve el primer elemento que se indica
mascotas.pop(1) #remueve en un indice indicado
del mascotas[0] #lo mismo que el de arriba
mascotas.clear() #vacia la lista
print(mascotas)