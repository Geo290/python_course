import math

print(round(1.3)) #redondea el numero
print(abs(-54)) #valor absoluto de un numero

print(math.ceil(1.1)) #envia el numero mayor entero mas cercano
print(math.floor(2.999)) #envial el numero menor entero mas cercano
print(math.isnan(23)) #retorna false si se trata de un numero
print(math.pow(10,2)) #eleva el numero de la izquierda a la potencia que hay en la derecha
print(math.sqrt(9)) #retorna la raiz cuadrada